let num1 = 8; 
let num2 = 10; 

console.log(`Numbers: num1 = ${num1}, num2 = ${num2}`);

// 1. Bitwise (&)

console.log(num1 & num2 );

// 2. Bitwise (|)
console.log(num1 | num2);

// 3. Bitwise (^)
console.log(num1 ^ num2);

// 4. Bitwise (~) on num1

console.log(num1);

// 5. Bitwise (~) on num2

console.log(num2);

// 6. Left Shift (<<) on num1

console.log(num1<< 1);

// 7. Right Shift (>>) on num1

console.log(num1 >> 1);

// 8. Left Shift (<<) on num2

console.log(num2<< 1);

// 9. Right Shift (>>) on num2

console.log(num2 >> 1);
